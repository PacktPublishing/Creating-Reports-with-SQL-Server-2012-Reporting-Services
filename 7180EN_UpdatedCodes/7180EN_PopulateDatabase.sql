go
declare @i integer=1
begin transaction
while @i<=100000
begin
  insert into dbo.tblCustomerPurchases values 
  (
    dateadd(dd,(round((1000-1-1)* rand() +1,0)),'01/01/2011'),
    round((1000-1-1)* rand() +1,0),
    round((1000-1-1)* rand() +1,0),
    round((120-1-1)* rand() +1,0),
    round((10-1-1)* rand() +1,0),
    round((200-10-1)* rand() +10,2)
  )
  set @i=@i+1
  if (@i%1000=0)
    begin
    commit
    begin transaction
  end
end
commit

select 'Updating customer purchases table' as MessageToUser
go

update dbo.tblCustomerPurchases
set AmountSpent=t1.QuantityPurchased*t2.ListPrice
from dbo.tblCustomerPurchases t1
join dbo.tblProduct t2 on t1.ProductKey=t2.ProductKey
go

