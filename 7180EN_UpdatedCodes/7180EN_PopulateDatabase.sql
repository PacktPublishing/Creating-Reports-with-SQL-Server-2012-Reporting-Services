(
  PurchaseDate date not null,
  CustomerKey int not null,
  FacilityKey int not null,
  ProductKey int not null,
  QuantityPurchased int not null,
  AmountSpent money not null
)
go
ALTER TABLE [dbo].tblCustomerPurchases  WITH CHECK ADD  CONSTRAINT [FK_tblCustomerPurchases_tblCustomer] FOREIGN KEY([CustomerKey])
REFERENCES [dbo].[tblCustomer] ([CustomerKey])
GO
ALTER TABLE [dbo].tblCustomerPurchases  WITH CHECK ADD  CONSTRAINT [FK_tblCustomerPurchases_tblFacility] FOREIGN KEY([FacilityKey])
REFERENCES [dbo].[tblFacility] ([FacilityKey])
GO
ALTER TABLE [dbo].tblCustomerPurchases  WITH CHECK ADD  CONSTRAINT [FK_tblCustomerPurchases_tblProduct] FOREIGN KEY([ProductKey])
REFERENCES [dbo].[tblProduct] ([ProductKey])
GO

